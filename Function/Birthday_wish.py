def birthday_wish(name, age):
    """
    Generates a birthday wish message.

    Parameters:
    name (str): The name of the person.
    age (int): The age of the person.

    Returns:
    str: The birthday wish message.
    """
    return f"Happy Birthday, {name}! Wishing you a fantastic day as you turn {age} years old. May your year be filled with joy, success, and good health!"

# Example usage:
name = "Alice"
age = 30
print(birthday_wish(name, age))
